        "    Args:\n",
        "        df: pandas.DataFrame, columns include at least [\"close\", \"volume\"]\n",
        "    Returns:\n",
        "        pandas.DataFrame\n",
        "    \"\"\"\n",
        "    df[\"dollar_pnl\"] = df[\"close\"].shift(1) - df[\"close\"]\n",
        "    df[\"on_balance_volume\"] = df[\"volume\"]\n",
        "    df[\"on_balance_volume\"] = df.apply(lambda row: row.volume * -1 if row.dollar_pnl < 0 else row.volume, axis=1)\n",
        "    df[\"on_balance_volume\"] = df[\"on_balance_volume\"].cumsum()\n",
        "    # print(df.head(10)[[\"date\", \"close\", \"dollar_pnl\", \"volume\", \"on_balance_volume\"]])\n",
        "    if produce_charts:\n",
        "        chart_on_balance_volume(df)\n",
