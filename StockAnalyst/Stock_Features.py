        "def rsi(df):\n",
        "    \"\"\"\n",
        "    Math reference: https://stockcharts.com/school/doku.php?id=chart_school:technical_indicators:relative_strength_index_rsi\n",
        "    Args:\n",
        "        df: pandas.DataFrame, columns include at least [\"close\"]\n",
        "    Returns:\n",
        "        pandas.DataFrame\n",
        "    \"\"\"\n",
        "    df[\"dollar_pnl\"] = df[\"close\"].shift(1) - df[\"close\"]\n",
        "    avg_gains = df[\"dollar_pnl\"].iloc[:14][df[\"dollar_pnl\"].iloc[:14] > 0].sum() / 14\n",
        "    avg_losses = abs(df[\"dollar_pnl\"].iloc[:14][df[\"dollar_pnl\"].iloc[:14] < 0].sum()) / 14\n",
        "    for i, row in df.iloc[14:].iterrows():\n",
        "        if row[\"dollar_pnl\"] > 0:\n",
        "            avg_gains = (avg_gains * 13 + row[\"dollar_pnl\"]) / 14\n",
        "        else:\n",
        "            avg_losses = (avg_losses * 13 + abs(row[\"dollar_pnl\"])) / 14\n",
        "        if avg_losses == 0:\n",
        "            rs = 100\n",
        "        else:\n",
        "            rs = avg_gains / avg_losses\n",
        "        df.loc[i, \"rsi\"] = 100 - 100 / (1 + rs)\n",
        "    # print(df.tail(20)[[\"date\", \"close\", \"rsi\"]])\n",
        "    if produce_charts:\n",
        "        chart_rsi(df)\n",
        "    return df\n",
