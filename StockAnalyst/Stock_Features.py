        "def parabolic_sar(df):\n",
        "  \"\"\"\n",
        "  Math reference: https://stockcharts.com/school/doku.php?id=chart_school:technical_indicators:parabolic_sar\n",
        "  Args:\n",
        "      df: pandas.DataFrame, columns include at least [\"close\"]\n",
        "  Returns:\n",
        "      pandas.DataFrame\n",
        "  \"\"\"\n",
        "  df[\"sar\"] = np.nan\n",
        "  step = 5\n",
        "  acc_factor = 0.02\n",
        "  uptrend = False\n",
        "  prior_sar = max(df.loc[1:step, \"close\"])\n",
        "  extreme_point = min(df.loc[1:step, \"close\"])\n",
        "  for i, row in df.iloc[step:].iterrows():\n",
        "      if uptrend:\n",
        "          df.at[i, \"sar\"] = prior_sar + acc_factor*(extreme_point - prior_sar)\n",
        "          if df.at[i, \"low\"] < df.at[i, \"sar\"]:\n",
        "              # reverse to downtrend\n",
        "              uptrend = False\n",
        "              prior_sar = max(df.loc[i-step:i, \"close\"])\n",
        "              extreme_point = min(df.loc[i-step:i, \"close\"])\n",
        "          else:\n",
        "              # continue uptrend\n",
        "              if df.at[i, \"close\"] > extreme_point:\n",
        "                  extreme_point = df.at[i, \"close\"]\n",
        "                  acc_factor = min(0.2, acc_factor+0.02)\n",
        "      else:\n",
        "          df.at[i, \"sar\"] = prior_sar - acc_factor*(prior_sar - extreme_point)\n",
        "          if df.at[i, \"high\"] > df.at[i, \"sar\"]:\n",
        "              # reverse to uptrend\n",
        "              uptrend = True\n",
        "              prior_sar = min(df.loc[i-step:i, \"close\"])\n",
        "              extreme_point = max(df.loc[i-step:i, \"close\"])\n",
        "          else:\n",
        "              # continue downtrend\n",
        "              if df.at[i, \"close\"] < extreme_point:\n",
        "                  extreme_point = df.at[i, \"close\"]\n",
        "                  acc_factor = min(0.2, acc_factor+0.02)\n",
        "      prior_sar = df.at[i, \"sar\"]\n",
        "  if produce_charts:\n",
        "      chart_sar(df)\n",
        "  return df\n",
        "\n",
